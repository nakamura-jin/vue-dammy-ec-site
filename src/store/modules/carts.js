export default {
  namespaced: true,

  /******************************************
   * state
   ******************************************/
  state: {
    carts: []
  },

  /******************************************
   * getters
   ******************************************/
  getters: {
    carts: (state) => state.carts,
  },

  /******************************************
   * mutations
   ******************************************/
  mutations: {
    CARTS(state, carts) {
      state.carts = carts;
    },
  },

  /******************************************
   * actions
   ******************************************/
  actions: {
    carts({ commit }, carts) {
      commit('CARTS', carts);
    },
  },
};
