export default {
  namespaced: true,

  /******************************************
   * state
   ******************************************/
  state: {
    carts: []
  },

  /******************************************
   * getters
   ******************************************/
  getters: {
    carts: (state) => state.carts,
  },

  /******************************************
   * mutations
   ******************************************/
  mutations: {
    CARTS(state, menu) {
      state.carts = menu
    }
  },

  /******************************************
   * actions
   ******************************************/
  actions: {
    carts({ commit }, menu) {
      commit('CARTS', menu);
    },
  },
};
